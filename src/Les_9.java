// Цикл for

public class Les_9 {
    public static void main(String[] args) {
        for (int num = 0; num < 5; num ++){  // пока num < 5, прибавляем 1 и выводим на экран
            System.out.println(num);
        };

        System.out.println("-----------");

        for (int i = 100; i > 9; i/=2) {  // Цикл можно писать сокращенно - fori
            System.out.println(i);
        }
    }
}
