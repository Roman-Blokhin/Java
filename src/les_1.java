// ПЕРЕМЕННЫЕ И ТИПЫ ДАННЫХ

public class les_1 {
    public static void main(String[] args) {
        int num = 78;
        System.out.println(num);
        System.out.println("My age: " + num + ".");

        float num_1 = 5.098f;  // обязательно указываем в конце f

        char ch = '&';  // хранит один символ в одинарных кавычках
        String word = "Roman boolean";  // строковый формат хранения данных
        boolean yes = true;  // булевое значение

        System.out.println(num_1 + ch + word + yes);

    }
}
